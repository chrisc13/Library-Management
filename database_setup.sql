-- this extension allows for uuids to be generated when rows are inserted into a table
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

CREATE TABLE Card(
    cardNumber varchar(12) PRIMARY KEY,
    pin integer NOT NULL,
    issueDate date
);

CREATE TABLE Genre(
	genreCode varchar(3) PRIMARY KEY,
	type text
);

CREATE TABLE Cardholder(
    cardHolderID UUID DEFAULT uuid_generate_v4() PRIMARY KEY,
    name text,
    age integer,
    cardNumber varchar(12),
    FOREIGN KEY(cardNumber) REFERENCES Card(cardNumber) ON DELETE CASCADE
);

CREATE TABLE Book( 
    bookID UUID DEFAULT uuid_generate_v4() PRIMARY KEY,
    title text, 
    checkOutDate date,
    dueDate date,
    format varchar(50),
    isAdult boolean,
    readLevel varchar(2),
	author text,
	ISBN varchar(17),
    checkedOutBy varchar(12),
    genreCode varchar(3),
    FOREIGN KEY(checkedOutBy) REFERENCES Card(cardNumber) ON DELETE CASCADE,
    FOREIGN KEY(genreCode) REFERENCES Genre
);

CREATE TABLE Movie( 
    movieID UUID DEFAULT uuid_generate_v4() PRIMARY KEY,
    title text,  
    checkOutDate date,
    dueDate date,
    format varchar(50),
    isAdult boolean,
    year integer,
    rating varchar(9),
    checkedOutBy varchar(12),
    genreCode varchar(3),
    FOREIGN KEY(checkedOutBy) REFERENCES Card(cardNumber) ON DELETE CASCADE,
    FOREIGN KEY(genreCode) REFERENCES Genre
);

